    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        String firstName = fNameInput.getText();
        String secondName = sNameInput.getText();
        Double percent = Double.parseDouble(percentInput.getText());
        if(firstName.isEmpty()){
            JOptionPane.showMessageDialog(this,"first name is empty");
        }else if(secondName.isEmpty()){
            JOptionPane.showMessageDialog(this,"last name field is empty");
        }else if(percentInput.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"percentage field is empty");
        }else{
            try {
                PhotogragherInterface ph = new Photogragher(firstName, secondName,pd.getPhotogragher().getAccount(),percent,pd.getPhotogragher().getId());
               int res = app.updatePhotographer(ph);
               if(res>=0){
                   app.sendMessageToAll("update photographer successfully");
                   app.refreshPhotographers(app.sendAllPhotographers());
                   pd.getPhotogragher().setFirstName(firstName);
                   pd.getPhotogragher().setSecondName(secondName);
                   pd.getPhotogragher().setPercent(percent);
               }else{
                   app.sendMessageToAll("photographer update failed");
               }
            } catch (RemoteException ex) {
                Logger.getLogger(PhotographerGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_editButtonActionPerformed

    private void withdrowButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrowButtonActionPerformed
        try {
            // TODO add your handling code here:
            Double ammount = Double.parseDouble(JOptionPane.showInputDialog(this,"Enter your quantity"));
            if(ammount.isNaN() || ammount < 0 ){
                JOptionPane.showMessageDialog(this,"your amount incurrect");
            }else if(ammount > pd.getPhotogragher().getAccount()){
                JOptionPane.showMessageDialog(this,"your amount is more than your account");
            }else{
                pd.getPhotogragher().withdraw(ammount);
//                JOptionPane.showMessageDialog(this,"your account is "+pd.getPhotogragher().getAccount());
                app.withDrowAccountPhotographer(pd.getPhotogragher(), ammount);
                accountInput.setText(pd.getPhotogragher().getAccount()+"");
            }
        } catch (RemoteException ex) {
            Logger.getLogger(PhotographerGUI.class.getName()).log(Level.SEVERE, null, ex);
        }catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(this,"your amount incurrect");
        }
    }//GEN-LAST:event_withdrowButtonActionPerformed

