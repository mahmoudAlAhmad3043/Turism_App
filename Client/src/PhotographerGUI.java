    @Override
    public void recivePhotographers(ArrayList<PhotogragherInterface> phs) throws RemoteException {
        boolean b = false;
//        photographers = app.sendAllPhotographers();
        photographers = phs;
        for(PhotogragherInterface ph :photographers){
            if(ph.getId() == pd.getPhotogragher().getId()){
                pd.setPhotogragher(ph);
                this.fNameInput.setText(pd.getPhotogragher().getFirstName());
                this.sNameInput.setText(pd.getPhotogragher().getSecondName());
                this.percentInput.setText(pd.getPhotogragher().getPercent()+"");
                this.accountInput.setText(pd.getPhotogragher().getAccount()+"");
                b=true;
                break;
            }
        }
        if(!b){
             try {
                    new PhotograghersGUI().setVisible(true);
                } catch (NotBoundException ex) {
                    Logger.getLogger(PhotographerGUI.class.getName()).log(Level.SEVERE, null, ex);
                }
                app.logout(this);
                this.dispose();
        }
        
    }

